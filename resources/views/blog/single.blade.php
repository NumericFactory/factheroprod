					<!-- CONTENU DE L'ARTICLE -->
					<h2>Le contexte - la loi</h2>
					<p class="graf graf--p graf-after--p">Tout d'abord, rappelons le contexte. L'Article 88 de la loi de finances 2016 doit entrer en vigueur au 1er Janvier 2018 : cette loi a pour objectif affiché de lutter contre la fraude à la TVA, en obligeant les entrepreneurs à utiliser des logiciels d'encaissement et de facturation, "certifiés".</p>

					<p class="graf graf--p graf-after--p">Autoentrepreneur, et créateur de factureHero, j'ai à plusieurs reprises exprimé mon désaccord total contre cette loi pour une raison simple : j'ai créé une solution de facturation pour faciliter la vie des autoentrepreneurs qui en ressentent le besoin, à l'exact inverse d'une obligation qui serait appliquée à tous, et qui n'aurait pas de sens. Pour la liberté de choix, pas la contrainte.</p>

					<h2>Pourquoi je suis opposé à cette loi ?</h2>

			
					<p class="graf graf--p graf-after--p">Une telle obligation viendrait s'ajouter à la longue liste de celles déjà imposées ces dernières années pour les Autoentrepreneurs (Contribution foncière des entreprises, obligation de formation préalable à l'installation pour les Artisans, cotisation pour frais de chambre, etc...).</p>

					<p class="graf graf--p graf-after--p">Cette obligation participerait également d'une stigmatisation toujours plus poussée des entrepreneurs, ici assimilés à priori à des fraudeurs.</p>

					<p class="graf graf--p graf-after--p">Et enfin, je pense absolument irréaliste et contre-productive cette mesure. En effet, il faut bien distinguer le logiciel d'encaissement (logiciels de caisse),  d'un logiciel de facturation. La majorité des fraudes contre lesquelles l'Etat essaie de lutter sont des fraudes de type "effacement après encaissement". Or, ces fraudes concernent bien une minorité de logiciels de caisse.</p>
					

					<p class="graf graf--p graf-after--p">J'ai créé FactureHero dans le but d'aider les Autoentrepreneurs dans leurs tâches de facturation-devis-comptabilité. Quand certains gros éditeurs avaient ici bien l'intention de profiter d'un marché captif d'autoentrepreneurs "obligés" d'acheter leur solution.</p>

					<h2>Bonne nouvelle : le gouvernement revient sur cette obligation</h2>

					<p class="graf graf--p graf-after--p">Heureusement, la polémique soulevée par cette "nouvelle obligation" a été entendue par le Gouvernement Macron.

					En effet, un communiqué de presse du Ministre des Comptes Publics <u>Gérald Darmanin</u>, indique clairement : <br>
					<span class="markup--quote markup--p-quote is-other">"Seuls les logiciels et systèmes de caisse, principaux vecteurs des fraudes constatées à la TVA, seront ainsi concernés"</span> </p>

					<p class="graf graf--p graf-after--p">Il est aussi très important de noter que : <span class="markup--quote markup--p-quote is-other">"Cette modification fera l’objet de mesures législatives d’ici la fin d’année, pour une entrée en vigueur du dispositif comme prévu au 1er janvier 2018"</span>.</p>

					<p class="graf graf--p graf-after--p">Tous les Autoentrepreneurs devront par conséquent être vigilants et veiller à la bonne application dans la loi de cette modification qui n'est pour le moment qu'une intention.</p>

					<p class="graf graf--p graf-after--p"><strong>C'est en tout cas une très bonne nouvelle. Partagez et informez vos amis entrepreneurs le plus largement possible et <u>donnez votre avis en commentaire</u></strong>.</p>

